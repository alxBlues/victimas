  <p><em>Parágrafo 4: </em>Es compromiso del usuario de MAAT, cambiar la contraseña  siempre que el sistema así lo solicite, sin usar en dicho caso, contraseñas previamente  usadas. En el mismo sentido, es compromiso del usuario realizar el cambio de  contraseña bajo el conocimiento de ocurrencia de un evento que comprometió la  seguridad del sistema, y en todo caso procurar la adopción del hábito de cambio  frecuente de contraseña.</p>
  <p><em>Parágrafo 5: </em>El usuario de MAAT, acepta el principio de circulación  restringida por el cual la información que cuenta con esta clasificación solo  será compartida con las entidades o personas autorizadas por el Municipio de  Acacías. </p>
  <p><strong>TERCERO: </strong>PROHIBICIÓN DE  REPRODUCCIÓN<strong> </strong></p>
  <p>En cumplimiento de los requisitos legales y reglamentarios relacionados con  las políticas de seguridad de la información, está prohibida la copia,  divulgación o uso indebido de la información obtenida o generada en el  cumplimiento de la obligación con el Municipio de Acacías. </p>
  <p>El usuario no debe reproducir por medio alguno la información  individualizada y personal, almacenada en medio físico o digital en  aplicativos, herramientas o cualquier medio. </p>
  <p>El usuario debe salvaguardar la reserva legal de la información a la que  tenga acceso.</p>
  <p><em>Parágrafo 1: </em>Toda información confidencial entregada por el Municipio  de Acacías es propiedad exclusiva de éste, y se revela únicamente con el  propósito de facilitar el desarrollo de los temas acordados entre las partes.</p>
  <p>Parágrafo 2: En caso que se compruebe incumplimiento de  la presente obligación, existiendo suministro de información reservada a un  tercero, el usuario deberá indemnizar todos los perjuicios que sean causados  por la divulgación, uso indebido o no autorizado, o aprovechamiento a favor  propio o de terceros, entre otros.</p>
  <p>Parágrafo 3: Sólo se podrá entregar información a un  tercero, previa autorización expresa y por escrito del Municipio de Acacías.</p>
  <p>De igual manera se prohíbe la copia, divulgación o uso  indebido de códigos fuente y/o</p>
