  <p align="center"><strong>ACUERDO DE  CONFIDENCIALIDAD DE USUARIOS DEL APLICATIVO MAAT DE EL MUNICIPIO DE ACACÍAS</strong></p>
  <br>
  <p> A continuación, se presentan las obligaciones relacionadas  con la confidencialidad de la información que deberán aceptar los usuarios al  momento de realizar la solicitud de acceso a la información o a la plataforma de  consulta y gestión MAAT del municipio de Acacías – Meta.<br>
  </p>
  <p align="center"><strong>CONSIDERACIONES</strong></p>
  <p><br>
    Que el  Artículo 15 de la Constitución Política dispone que todas las personas tienen  derecho a su intimidad personal y familiar y a su buen nombre, y el Estado debe  respetarlos y hacerlos respetar. De igual modo, tienen derecho a conocer,  actualizar y rectificar las informaciones que se hayan recogido sobre ellas en  bancos de datos y en archivos de entidades públicas y privadas.<br>
    Que  el Artículo 156, parágrafo 1º de la Ley 1448 de 2011, estableció la Reserva  Legal para el Registro Único de Víctimas, 
    &ldquo; 
    <em>con  el fin de proteger el derecho a la intimidad de las víctimas y su seguridad</em>
    &rdquo;
    .</p>
  <p>Que  no obstante lo anterior, el Artículo 20 de la Constitución Política contempla  el derecho que tiene la población colombiana de informar y recibir información  veraz e imparcial, y en desarrollo de esta garantía constitucional, la misma Carta  Política en su Artículo 74 dispone que
    &ldquo; 
    <em>todas  las personas tienen derecho a acceder a los documentos públicos salvo los casos  que establezca la ley</em>
    &rdquo;
    .</p>
  <p>Que  en este contexto, se expidió la Ley 1266 de 2008 con el fin de
    &ldquo; 
    <em>desarrollar el derecho constitucional que  tienen todas las personas a conocer, actualizar y rectificar las informaciones  que se hayan recogido sobre ellas en bancos de datos, y los demás derechos,  libertades y garantías constitucionales relacionadas con la recolección,  tratamiento y circulación de datos personales a que se refiere el artículo 15  de la Constitución Política, así como el derecho a la información establecido  en el artículo 20 de la Constitución Política, particularmente en relación con  la información financiera y crediticia, comercial, de servicios y la  proveniente de terceros países</em>
    &rdquo;
    , en la cual se expresa el carácter  confidencial de toda la información financiera, crediticia y comercial de los  titulares de la información.</p>
  <p>Que  con la Ley 1581 de 2012 se expidió el régimen general de protección de datos  personales de todas las personas que se hayan recogido sobre ellas en bases de  datos o archivos.</p>
