<div style="width: 650px;height: 676px;text-align:justify;">
  <p>Que  el Artículo 10 de la Ley 1581 de 2012 dispuso que no se requiere autorización  por parte del titular de la información cuando la información sea requerida por  una entidad pública o administrativa en ejercicio de sus funciones legales o  por orden judicial.</p>
  <p>Que  de conformidad con la Ley 599 de 2000, Código Penal, en su Artículo 194,  modificado por el Artículo 25 de la Ley 1288 de 2009 referente a las conductas  punibles que atenten en contra de la intimidad, reserva e interceptación de  comunicaciones, establece que:
    &ldquo; 
    <em>Divulgación  y empleo de documentos reservados.</em> <em>El  que en provecho propio o ajeno o con perjuicio de otro divulgue o emplee el  contenido de un documento que deba permanecer en reserva, incurrirá en multa,  siempre que la conducta no constituya delito sancionado con pena mayor</em>
    &rdquo;
    .</p>
  <p>Que  la Ley 1273 de 2009, modificó el Código Penal y creó un nuevo bien jurídico  tutelado denominado
    &quot; 
    <em>de la  protección de la información y de los datos</em>
    &rdquo;
    , además de establecer  disposiciones que busquen preservar integralmente los sistemas que utilicen las  tecnologías de la información y las comunicaciones. Así, en su Artículo 269A  establece <em>
    &ldquo;
    El que, sin autorización o por  fuera de lo acordado, acceda en todo o en parte a un sistema informático  protegido o no con una medida de seguridad, o se mantenga dentro del mismo en  contra de la voluntad de quien tenga el legítimo derecho a excluirlo, incurrirá  en pena de prisión de cuarenta y ocho (48) a noventa y seis (96) meses y en  multa de 100 a 1.000 salarios mínimos legales mensuales vigentes.
    &rdquo; 
    </em></p>
  <p>De  igual manera, en su Artículo 269F señaló:
    &ldquo; 
    <em>El  que, sin estar facultado para ello, con provecho propio o de un tercero,  obtenga, compile, sustraiga, ofrezca, venda, intercambie, envíe, compre,  intercepte, divulgue, modifique o emplee códigos personales, datos personales  contenidos en ficheros, archivos, bases de datos o medios semejantes, incurrirá  en pena de prisión de cuarenta y ocho (48) a noventa y seis (96) meses y en  multa de 100 a 1000 salarios mínimos legales mensuales vigentes.</em>
    &rdquo; 
  </p>
  <p>Que con fundamento en lo anterior, y en virtud de la  reserva legal de la información asociada a la población Víctima, sobre la  protección de los documentos e información de la misma contempladas en el marco  del artículo 31 de la Ley 1448 de 2011, y, atendiendo la jurisprudencia de la  Corte Constitucional que establece que dicha información guarda una estrecha  relación con los derechos fundamentales de sus titulares, tales como la vida,  la intimidad y la seguridad personal, por lo cual sobre la misma opera una  Reserva Constitucional; se suscribe el presente documento.</p>
  <p>En consecuencia, aceptar el ingreso al aplicativo MAAT implica la 
    &ldquo;
    Aceptación  del <strong><u>acuerdo de confidencialidad de usuarios del aplicativo MAAT de el  Municipio de Acacías</u></strong>
    &rdquo; 
  </p>
