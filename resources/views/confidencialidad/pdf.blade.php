  <p><strong>CUARTO: </strong>ACCESO Y USO DE APLICATIVOS Y HERRAMIENTAS  TECNOLÓGICAS</p>
  <p><a name="bookmark2">El usuario de MAAT se compromete a cumplir todos  los protocolos operativos asociados a su uso y administración.</a></p>
  <p><em>Parágrafo 1: </em>Es compromiso del usuario conocer y apropiar  los protocolos asociados al manejo operativo y administrativo de las herramientas  tecnológicas a las cuales tenga acceso.</p>
  <p><em>Parágrafo 2: </em>El usuario de MAAT acepta que el  administrador del aplicativo proceda con el bloqueo de la cuenta cuando éste  último evidencie una falta de uso fuera de los límites permitidos de  inactividad o cuando el Municipio de Acacías así lo requiera.</p>
  <p><em>Parágrafo 3: </em>El usuario de MAAT informará al administrador  del sistema, a través de los canales establecidos para tal fin, cuando se  presente algún bloqueo o incidencia en la cuenta de usuario de manera que se  puedan adelantar las acciones pertinentes.</p>
  <p><em>Parágrafo 4: </em>El usuario de MAAT acepta cualquier cambio de  perfil de acceso a los mismos cuando así se disponga.</p>
  <p><a name="bookmark8"><strong>QUINTO</strong></a>:  VIGENCIA </p>
  <p>Lo señalado en  el presente documento tendrá la duración definida en la fecha de terminación  del contrato, incluidas sus prórrogas o vinculación del usuario señalada en el
    &ldquo;
    Formato digital de aceptación del lineamiento de Confidencialidad de usuario  de MAAT del Municipio de Acacías
    &rdquo;
    . </p>
  <p>Es de pleno  entendimiento que el acceso y uso de los Aplicativos y Herramientas  tecnológicas, por parte del usuario, es exclusivo durante la vigencia de la  vinculación con el Municipio de Acacías o del tercero, acorde con las  actividades que así lo ameriten. </p>
  <p>Terminado el  presente compromiso, no se dará por terminada la obligación de dar aplicación  de los principios de confidencialidad, reserva, circulación restringida y  salvaguarda de la información, por lo cual se deberá garantizar la protección  de la misma de conformidad con lo dispuesto en la Ley 1581 de 2012 y demás  normas concordantes.</p>
