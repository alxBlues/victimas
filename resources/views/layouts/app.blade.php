$(document).ready(function() {

 $('select[name="planes"]').on('change', function(){
    var tipo = document.getElementById('tipo').value;
     var plan = $(this).val();
     if(plan) {
         $.ajax({

             url: '/variables/get/'+plan+'/'+tipo,
             type:"GET",
             dataType:"json",
             beforeSend: function(){
                 $('#loader').css("visibility", "visible");
             },

             success:function(data) {
                 $('#entregables').show();
                 $('select[name="a_entregables"]').empty();
                 $('select[name="a_entregables"]').append('<option value="0">Seleccionar</option>');
                 $.each(data, function(key, value){

                     $('select[name="a_entregables"]').append('<option value="'+ key +'">' + value + '</option>');

                 });
             },
             complete: function(){
                 $('#loader').css("visibility", "hidden");
             }
         });
     } else {
         $('select[name="a_entregables"]').empty();

     }

 });

});
</script>

