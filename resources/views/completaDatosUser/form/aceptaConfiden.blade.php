    ACUERDO DE CONFIDENCIALIDAD DE USUARIOS DEL APLICATIVO MAAT DE EL MUNICIPIO DE ACACÍAS
    <p style="text-align: justify;">A continuación, se presentan las obligaciones relacionadas con la confidencialidad de la información que deberán aceptar los usuarios al momento de realizar la solicitud de acceso a la información o a la plataforma de consulta y gestión MAAT del municipio de Acacías – Meta.
    CONSIDERACIONES
    Que el Artículo 15 de la Constitución Política dispone que todas las personas tienen derecho a su intimidad personal y familiar y a su buen nombre, y el Estado debe respetarlos y hacerlos respetar. De igual modo, tienen derecho a conocer, actualizar y rectificar las informaciones que se hayan recogido sobre ellas en bancos de datos y en archivos de entidades públicas y privadas.
    Que el Artículo 156, parágrafo 1º de la Ley 1448 de 2011, estableció la Reserva Legal para el Registro Único de Víctimas, “con el fin de proteger el derecho a la intimidad de las víctimas y su seguridad”.

    Que no obstante lo anterior, el Artículo 20 de la Constitución Política contempla el derecho que tiene la población colombiana de informar y recibir información veraz e imparcial, y en desarrollo de esta garantía constitucional, la misma Carta Política en su Artículo 74 dispone que “todas las personas tienen derecho a acceder a los documentos públicos salvo los casos que establezca la ley”.

    Que en este contexto, se expidió la Ley 1266 de 2008 con el fin de “desarrollar el derecho constitucional que tienen todas las personas a conocer, actualizar y rectificar las informaciones que se hayan recogido sobre ellas en bancos de datos, y los demás derechos, libertades y garantías constitucionales relacionadas con la recolección, tratamiento y circulación de datos personales a que se refiere el artículo 15 de la Constitución Política, así como el derecho a la información establecido en el artículo 20 de la Constitución Política, particularmente en relación con la información financiera y crediticia, comercial, de servicios y la proveniente de terceros países”, en la cual se expresa el carácter confidencial de toda la información financiera, crediticia y comercial de los titulares de la información.

    Que con la Ley 1581 de 2012 se expidió el régimen general de protección de datos personales de todas las personas que se hayan recogido sobre ellas en bases de datos o archivos.

    Que el Artículo 10 de la Ley 1581 de 2012 dispuso que no se requiere autorización por parte del titular de la información cuando la información sea requerida por una entidad pública o administrativa en ejercicio de sus funciones legales o por orden judicial.

    Que de conformidad con la Ley 599 de 2000, Código Penal, en su Artículo 194, modificado por el Artículo 25 de la Ley 1288 de 2009 referente a las conductas punibles que atenten en contra de la intimidad, reserva e interceptación de comunicaciones, establece que: “Divulgación y empleo de documentos reservados. El que en provecho propio o ajeno o con perjuicio de otro divulgue o emplee el contenido de un documento que deba permanecer en reserva, incurrirá en multa, siempre que la conducta no constituya delito sancionado con pena mayor”.

    Que la Ley 1273 de 2009, modificó el Código Penal y creó un nuevo bien jurídico tutelado denominado "de la protección de la información y de los datos”, además de establecer disposiciones que busquen preservar integralmente los sistemas que utilicen las tecnologías de la información y las comunicaciones. Así, en su Artículo 269A establece “El que, sin autorización o por fuera de lo acordado, acceda en todo o en parte a un sistema informático protegido o no con una medida de seguridad, o se mantenga dentro del mismo en contra de la voluntad de quien tenga el legítimo derecho a excluirlo, incurrirá en pena de prisión de cuarenta y ocho (48) a noventa y seis (96) meses y en multa de 100 a 1.000 salarios mínimos legales mensuales vigentes.”

    De igual manera, en su Artículo 269F señaló: “El que, sin estar facultado para ello, con provecho propio o de un tercero, obtenga, compile, sustraiga, ofrezca, venda, intercambie, envíe, compre, intercepte, divulgue, modifique o emplee códigos personales, datos personales contenidos en ficheros, archivos, bases de datos o medios semejantes, incurrirá en pena de prisión de cuarenta y ocho (48) a noventa y seis (96) meses y en multa de 100 a 1000 salarios mínimos legales mensuales vigentes.”

    Que con fundamento en lo anterior, y en virtud de la reserva legal de la información asociada a la población Víctima, sobre la protección de los documentos e información de la misma contempladas en el marco del artículo 31 de la Ley 1448 de 2011, y, atendiendo la jurisprudencia de la Corte Constitucional que establece que dicha información guarda una estrecha relación con los derechos fundamentales de sus titulares, tales como la vida, la intimidad y la seguridad personal, por lo cual sobre la misma opera una Reserva Constitucional; se suscribe el presente documento.

    En consecuencia, aceptar el ingreso al aplicativo MAAT implica la “Aceptación del acuerdo de confidencialidad de usuarios del aplicativo MAAT de el Municipio de Acacías” contenido en el presente documento, en condición de usuario del sistema, por lo cual acepta conocer las anteriores consideraciones y adquiere el compromiso de cumplir lo estipulado en los siguientes numerales:

    PRIMERO: PROTECCIÓN DE DATOS PERSONALES

    La protección de los datos personales de la población víctima contenidos en las herramientas tecnológicas o en cualquier mecanismo físico o digital, es el principal compromiso de todos los usuarios que accedan a ellos, toda vez que son catalogados como datos sensibles con carácter reservado y confidencial, y su tratamiento está sujeta a la normatividad aplicable.

    SEGUNDO: SEGURIDAD Y SALVAGUARDA DE LA INFORMACIÓN.

    El usuario debe realizar la adecuada utilización de contraseñas para el acceso al aplicativo MAAT. Las contraseñas son sujetas a una completa confidencialidad y en ningún caso deberán ser divulgadas o compartidas, en forma verbal o escrita o por cualquier otro medio de difusión digital o análogo.

    En este sentido, es compromiso del usuario de MAAT, garantizar la confidencialidad, reserva y seguridad de la información y abstenerse de hacer uso de la información o del proceso de diligenciamiento para obtener provecho para sí o para terceros.

    Parágrafo 1. El titular del usuario asignado se hará responsable de todas las actividades realizadas a través de su cuenta.

    Parágrafo 2. La contraseña será generada y comunicada en respuesta a la solicitud de creación del usuario, al correo electrónico institucional del municipio o al correo empresarial o institucional de terceras partes que suscriban con el Municipio de Acacías, un convenio, contrato o cualquier acuerdo de voluntades.
    Parágrafo 3: Es compromiso del usuario de MAAT cambiar la contraseña inicial dentro de las 8 horas siguientes después de ser recibida. La contraseña debe seguir estándares de seguridad y que como mínimo cumpla con:

    I. Una longitud mínima de 7 caracteres alfanuméricos.
    II. No contendrá nombres, números de teléfono, palabras, números o letras consecutivas repetidas.
    III. Debe contener al menos un número, una letra minúscula y otra mayúscula.

    Parágrafo 4: Es compromiso del usuario de MAAT, cambiar la contraseña siempre que el sistema así lo solicite, sin usar en dicho caso, contraseñas previamente usadas. En el mismo sentido, es compromiso del usuario realizar el cambio de contraseña bajo el conocimiento de ocurrencia de un evento que comprometió la seguridad del sistema, y en todo caso procurar la adopción del hábito de cambio frecuente de contraseña.

    Parágrafo 5: El usuario de MAAT, acepta el principio de circulación restringida por el cual la información que cuenta con esta clasificación solo será compartida con las entidades o personas autorizadas por el Municipio de Acacías.

    TERCERO: PROHIBICIÓN DE REPRODUCCIÓN

    En cumplimiento de los requisitos legales y reglamentarios relacionados con las políticas de seguridad de la información, está prohibida la copia, divulgación o uso indebido de la información obtenida o generada en el cumplimiento de la obligación con el Municipio de Acacías.

    El usuario no debe reproducir por medio alguno la información individualizada y personal, almacenada en medio físico o digital en aplicativos, herramientas o cualquier medio.

    El usuario debe salvaguardar la reserva legal de la información a la que tenga acceso.

    Parágrafo 1: Toda información confidencial entregada por el Municipio de Acacías es propiedad exclusiva de éste, y se revela únicamente con el propósito de facilitar el desarrollo de los temas acordados entre las partes.

    Parágrafo 2: En caso que se compruebe incumplimiento de la presente obligación, existiendo suministro de información reservada a un tercero, el usuario deberá indemnizar todos los perjuicios que sean causados por la divulgación, uso indebido o no autorizado, o aprovechamiento a favor propio o de terceros, entre otros.

    Parágrafo 3: Sólo se podrá entregar información a un tercero, previa autorización expresa y por escrito del Municipio de Acacías.

    De igual manera se prohíbe la copia, divulgación o uso indebido de códigos fuente y/o bases de datos de sistemas de información de la Entidad, así como información asociada a la ejecución de planes o proyectos, en caso que el usuario tenga acceso.

    CUARTO: ACCESO Y USO DE APLICATIVOS Y HERRAMIENTAS TECNOLÓGICAS

    El usuario de MAAT se compromete a cumplir todos los protocolos operativos asociados a su uso y administración.

    Parágrafo 1: Es compromiso del usuario conocer y apropiar los protocolos asociados al manejo operativo y administrativo de las herramientas tecnológicas a las cuales tenga acceso.

    Parágrafo 2: El usuario de MAAT acepta que el administrador del aplicativo proceda con el bloqueo de la cuenta cuando éste último evidencie una falta de uso fuera de los límites permitidos de inactividad o cuando el Municipio de Acacías así lo requiera.

    Parágrafo 3: El usuario de MAAT informará al administrador del sistema, a través de los canales establecidos para tal fin, cuando se presente algún bloqueo o incidencia en la cuenta de usuario de manera que se puedan adelantar las acciones pertinentes.

    Parágrafo 4: El usuario de MAAT acepta cualquier cambio de perfil de acceso a los mismos cuando así se disponga.

    QUINTO: VIGENCIA

    Lo señalado en el presente documento tendrá la duración definida en la fecha de terminación del contrato, incluidas sus prórrogas o vinculación del usuario señalada en el “Formato digital de aceptación del lineamiento de Confidencialidad de usuario de MAAT del Municipio de Acacías”.

    Es de pleno entendimiento que el acceso y uso de los Aplicativos y Herramientas tecnológicas, por parte del usuario, es exclusivo durante la vigencia de la vinculación con el Municipio de Acacías o del tercero, acorde con las actividades que así lo ameriten.

    Terminado el presente compromiso, no se dará por terminada la obligación de dar aplicación de los principios de confidencialidad, reserva, circulación restringida y salvaguarda de la información, por lo cual se deberá garantizar la protección de la misma de conformidad con lo dispuesto en la Ley 1581 de 2012 y demás normas concordantes.

    Hace parte de este documento el formato digital de aceptación del acuerdo de confidencialidad de usuarios de MAAT del Municipio de Acacías.

    Este documento se basa en el Formato Acuerdo de Confidencialidad de Usuarios de Herramientas Tecnológicas o Información de la Unidad para la Atención y Reparación Integral a las Victimas, versión 1, publicado en Abril 20 de 2017 en www.unidadvictimas.gov.co, con los fines de coherencia a los lineamientos de dicha Unidad en el manejo de la información.
